# Takes two numbers from the user

num1 = int(input("Enter your first number: "))
num2 = int(input("Enter your second number: "))

# Sum
sum = num1 + num2
print("sum:", sum)

# Difference
diff = num1 - num2
print("Difference:", diff)

# Product 
prod = num1 * num2
print("Product:", prod)

# Division
div = num1 / num2
print("Division:", div)

# floor division
fdiv = num1 // num2
print("Floor Division:", fdiv)
